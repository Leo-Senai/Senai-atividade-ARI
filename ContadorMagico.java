package AulaCase;

public class ContadorMagico {
	public static void main(String[] args) {
		
		System.out.println("Conategem crescente : ");
		for(int i = 1; i<= 10;i++ ) {
			System.out.println(i);
		}
		
		
		System.out.println("Contagem decrescente :");
		for(int i= 10; i >= 1; i--) {
			System.out.println(i);
		}
		
	}

}
